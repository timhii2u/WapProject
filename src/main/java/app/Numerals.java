package app;

public class Numerals{
	
	private int number;
	private String value;
	
	public Numerals(int number,String value) {
		this.number = number;
		this.value = value;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	

}
